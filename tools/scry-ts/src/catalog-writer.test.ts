// --- Notes round-trip ---

describe("preserveNotes", () => {
  it("extracts notes from existing YAML", () => {
    const yaml = [
      "type: Foo",
      "instances: 10",
      "notes: |",
      "  Hand-written notes here.",
      "  Second line of notes.",
      "",
    ].join("\n");

    const notes = preserveNotes(yaml);
    expect(notes).toBe("Hand-written notes here.\nSecond line of notes.");
  });

  it("returns empty string when no notes", () => {
    const yaml = "type: Foo\ninstances: 10\n";
    expect(preserveNotes(yaml)).toBe("");
  });

  it("returns empty string for empty notes block", () => {
    const yaml = "type: Foo\nnotes: |\n";
    expect(preserveNotes(yaml)).toBe("");
  });
});

describe("notes round-trip", () => {
  it("preserves hand-written notes across regeneration", () => {
    const profile = makeAnnotationProfile();

    // First render — no notes
    const yaml1 = renderAnnotationYaml(profile);
    expect(yaml1).toContain("notes: |");

    // Simulate hand-editing: add notes
    const edited = yaml1.replace("notes: |\n", "notes: |\n  Important finding about CounterAdded.\n");

    // Re-render with existing content — notes preserved
    const yaml2 = renderAnnotationYaml(profile, edited);
    expect(yaml2).toContain("notes: |");
    expect(yaml2).toContain("  Important finding about CounterAdded.");
  });
});

// --- Task 3: Dictionaries ---

describe("collectDictionaryValues", () => {
  it("collects values from dictionary-relevant keys", () => {
    const annotations = new Map<string, AnnotationInstance[]>();
    annotations.set("CounterAdded", [
      {
        type: "CounterAdded",
        keys: ["counter_type"],
        values: { counter_type: 1 },
        isPersistent: false,
        gameLabel: "g1",
        gsmId: 1,
        affectorId: 100,
        affectedIds: [],
        coTypes: [],
      },
      {
        type: "CounterAdded",
        keys: ["counter_type"],
        values: { counter_type: 999 },
        isPersistent: false,
        gameLabel: "g1",
        gsmId: 2,
        affectorId: 101,
        affectedIds: [],
        coTypes: [],
      },
    ]);
    annotations.set("DamageDealt", [
      {
        type: "DamageDealt",
        keys: ["type"],
        values: { type: 1 },
        isPersistent: false,
        gameLabel: "g1",
        gsmId: 3,
        affectorId: 102,
        affectedIds: [],
        coTypes: [],
      },
    ]);

    const observed = collectDictionaryValues(annotations);

    expect(observed.get("counter_type")?.has("1")).toBe(true);
    expect(observed.get("counter_type")?.has("999")).toBe(true);
    expect(observed.get("damage_type")?.has("1")).toBe(true);
  });

  it("does not map 'type' key outside DamageDealt", () => {
    const annotations = new Map<string, AnnotationInstance[]>();
    annotations.set("SomeOther", [
      {
        type: "SomeOther",
        keys: ["type"],
        values: { type: 1 },
        isPersistent: false,
        gameLabel: "g1",
        gsmId: 1,
        affectorId: undefined,
        affectedIds: [],
        coTypes: [],
      },
    ]);

    const observed = collectDictionaryValues(annotations);
    expect(observed.has("damage_type")).toBe(false);
  });
});

describe("buildDictionaries", () => {
  it("merges observed with seed, unknown values get null", () => {
    const observed = new Map<string, Set<string>>();
    observed.set("counter_type", new Set(["1", "999"]));

    const dict = buildDictionaries(observed);

    // Seed value preserved
    expect(dict.counter_type["1"]).toBe("+1/+1");
    // Unknown value gets null
    expect(dict.counter_type["999"]).toBeNull();
    // Other seed dicts still present
    expect(dict.damage_type["1"]).toBe("combat");
  });
});

describe("renderDictionariesYaml", () => {
  it("produces valid YAML with sorted keys", () => {
    const dict = buildDictionaries(new Map([["counter_type", new Set(["999"])]]));
    const yaml = renderDictionariesYaml(dict);

    expect(yaml).toContain("# auto-generated by scry catalog");
    expect(yaml).toContain("counter_type:");
    expect(yaml).toContain('  "1": "+1/+1"');
    expect(yaml).toContain('  "999": null  # unknown');
    // Keys should be numerically sorted — "1" before "2" before "42"
    const lines = yaml.split("\n");
    const counterLines = lines.filter((l) => l.startsWith('  "') && lines.indexOf(l) > lines.indexOf("counter_type:"));
    // Just verify it contains expected entries
    expect(yaml).toContain("damage_type:");
    expect(yaml).toContain("mana_color:");
  });
});

// --- Task 4: Index README ---

describe("renderIndexReadme", () => {
  it("produces markdown with all three tables", () => {
    const annotations = new Map<string, AnnotationProfile>();
    annotations.set("CounterAdded", makeAnnotationProfile());
    annotations.set("DamageDealt", makeAnnotationProfile({
      type: "DamageDealt",
      instances: 50,
      games: 10,
      persistence: "transient",
      alwaysKeys: ["damage_amount"],
    }));

    const prompts = new Map<string, PromptProfile>();
    prompts.set("SelectNReq", makePromptProfile());

    const actions = new Map<string, ActionProfile>();
    actions.set("Cast", makeActionProfile());

    const md = renderIndexReadme(annotations, prompts, actions, 15, 200);

    // Header
    expect(md).toContain("# Protocol Catalog");
    expect(md).toContain("Corpus: 15 games, 200 game state messages.");

    // Annotations table
    expect(md).toContain("## Annotations");
    expect(md).toContain("| Type | Instances | Games | Persistence | Always keys |");
    expect(md).toContain("| DamageDealt | 50 |"); // higher count first
    expect(md).toContain("| CounterAdded | 23 |");

    // Prompts table
    expect(md).toContain("## Prompts");
    expect(md).toContain("| Type | Occurrences | Games | Always fields |");
    expect(md).toContain("| SelectNReq | 45 |");

    // Actions table
    expect(md).toContain("## Actions");
    expect(md).toContain("| Cast | 100 |");
  });
});
