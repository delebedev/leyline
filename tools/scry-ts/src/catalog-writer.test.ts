// --- Helpers ---

function makeAnnotationProfile(overrides?: Partial<AnnotationProfile>): AnnotationProfile {
  return {
    type: "CounterAdded",
    instances: 23,
    games: 8,
    persistence: "transient",
    alwaysKeys: ["counter_type", "transaction_amount"],
    sometimesKeys: [{ key: "some_rare_key", pct: 15 }],
    valueSamples: {
      counter_type: new Set(["1", "139", "42"]),
      transaction_amount: new Set(["1", "2", "3"]),
    },
    cards: {
      count: 9,
      notable: [
        { grp: 84262, name: "Enduring Bondwarden", context: 'counter_type=1 (backup)' },
      ],
    },
    coTypes: new Map([
      ["PowerToughnessModCreated", 20],
      ["Counter", 15],
    ]),
    firstSeen: "2026-03-29",
    lastSeen: "2026-04-04",
    ...overrides,
  };
}

