// --- YAML header ---

const YAML_HEADER = "# auto-generated by scry catalog — do not edit except `notes:` field\n";

// --- Notes round-trip ---

/**
 * Extract the `notes:` block scalar content from existing YAML.
 * Returns empty string if no notes block found.
 */
export function preserveNotes(existing: string): string {
  const match = existing.match(/^notes:\s*\|\n([\s\S]*)/m);
  if (!match) return "";
  // The block scalar content is everything after `notes: |\n`, indented by 2 spaces
  const lines = match[1].split("\n");
  const noteLines: string[] = [];
  for (const line of lines) {
    // Block scalar lines are indented (or empty)
    if (line.startsWith("  ") || line === "") {
      noteLines.push(line.startsWith("  ") ? line.slice(2) : line);
    } else {
      break; // next top-level key or end of file
    }
  }
  // Trim trailing empty lines
  while (noteLines.length > 0 && noteLines[noteLines.length - 1] === "") {
    noteLines.pop();
  }
  return noteLines.join("\n");
}

// --- YAML helpers ---

function yamlArray(items: string[]): string {
  if (items.length === 0) return "[]";
  return "[" + items.map((i) => JSON.stringify(i)).join(", ") + "]";
}

function yamlValueSamples(samples: Record<string, Set<string>>): string {
  const keys = Object.keys(samples).sort();
  if (keys.length === 0) return "value_samples: {}\n";
  let out = "value_samples:\n";
  for (const key of keys) {
    const vals = [...samples[key]].slice(0, 5);
    out += `  ${key}: ${yamlArray(vals)}\n`;
  }
  return out;
}

function yamlNotesBlock(notes: string): string {
  if (!notes) return "notes: |\n";
  return "notes: |\n" + notes.split("\n").map((l) => `  ${l}`).join("\n") + "\n";
}

// --- Annotation YAML ---

export function renderAnnotationYaml(
  profile: AnnotationProfile,
  existingContent?: string,
): string {
  const notes = existingContent ? preserveNotes(existingContent) : "";

  let out = YAML_HEADER;
  out += `type: ${profile.type}\n`;
  out += `instances: ${profile.instances}\n`;
  out += `games: ${profile.games}\n`;
  out += `persistence: ${profile.persistence}\n`;
  out += `always_keys: ${yamlArray(profile.alwaysKeys)}\n`;

  if (profile.sometimesKeys.length === 0) {
    out += "sometimes_keys: []\n";
  } else {
    out += "sometimes_keys:\n";
    for (const sk of profile.sometimesKeys) {
      out += `  - key: ${JSON.stringify(sk.key)}\n`;
      out += `    pct: ${sk.pct}\n`;
    }
  }

  out += yamlValueSamples(profile.valueSamples);

  // Cards
  out += "cards:\n";
  out += `  count: ${profile.cards.count}\n`;
  if (profile.cards.notable.length === 0) {
    out += "  notable: []\n";
  } else {
    out += "  notable:\n";
    for (const n of profile.cards.notable) {
      out += `    - { grp: ${n.grp}, name: ${JSON.stringify(n.name)}, context: ${JSON.stringify(n.context)} }\n`;
    }
  }

  // Co-types: sorted by count desc
  const coTypeSorted = [...profile.coTypes.entries()].sort((a, b) => b[1] - a[1]);
  const coTypeNames = coTypeSorted.map(([name]) => name);
  out += `co_types: ${yamlArray(coTypeNames)}\n`;

  out += `first_seen: ${profile.firstSeen ?? "null"}\n`;
  out += `last_seen: ${profile.lastSeen ?? "null"}\n`;
  out += yamlNotesBlock(notes);

  return out;
}

