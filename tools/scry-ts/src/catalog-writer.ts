// --- Dictionaries (Task 3) ---

/**
 * Scan annotations for dictionary-relevant detail keys.
 * Returns a map from dictionary name → set of observed values.
 */
export function collectDictionaryValues(
  annotations: Map<string, AnnotationInstance[]>,
): Map<string, Set<string>> {
  const observed = new Map<string, Set<string>>();

  for (const [annType, instances] of annotations) {
    for (const inst of instances) {
      for (const [key, val] of Object.entries(inst.values)) {
        let dictName: string | undefined;

        if (key === "type" && annType === "DamageDealt") {
          dictName = "damage_type";
        } else {
          dictName = KEY_TO_DICT[key];
        }

        if (!dictName) continue;

        let set = observed.get(dictName);
        if (!set) {
          set = new Set();
          observed.set(dictName, set);
        }
        set.add(String(val));
      }
    }
  }

  return observed;
}

/**
 * Merge observed values with seed data.
 * Unknown values (observed but not in seed) get null label.
 */
export function buildDictionaries(
  observedValues: Map<string, Set<string>>,
): Dictionaries {
  const result: Dictionaries = {};

  // Start with all seed dictionaries
  for (const [dictName, seedEntries] of Object.entries(SEED)) {
    result[dictName] = { ...seedEntries };
  }

  // Merge observed values
  for (const [dictName, values] of observedValues) {
    if (!result[dictName]) {
      result[dictName] = {};
    }
    for (const val of values) {
      if (!(val in result[dictName])) {
        result[dictName][val] = null; // unknown
      }
    }
  }

  return result;
}

export function renderDictionariesYaml(dict: Dictionaries): string {
  let out = YAML_HEADER;

  const dictNames = Object.keys(dict).sort();
  for (const name of dictNames) {
    out += `${name}:\n`;
    const entries = dict[name];
    // Sort keys numerically
    const keys = Object.keys(entries).sort((a, b) => Number(a) - Number(b));
    for (const key of keys) {
      const label = entries[key];
      if (label === null) {
        out += `  "${key}": null  # unknown — observed in corpus\n`;
      } else {
        out += `  "${key}": ${JSON.stringify(label)}\n`;
      }
    }
  }

  return out;
}

export function writeDictionaries(dict: Dictionaries, outDir: string): void {
  ensureDir(outDir);
  const filePath = join(outDir, "dictionaries.yaml");
  writeFileSync(filePath, renderDictionariesYaml(dict));
}

// --- Index README (Task 4) ---

export function renderIndexReadme(
  annotations: Map<string, AnnotationProfile>,
  prompts: Map<string, PromptProfile>,
  actions: Map<string, ActionProfile>,
  gameCount: number,
  gsmCount: number,
): string {
  let out = "# Protocol Catalog\n\n";
  out += `Corpus: ${gameCount} games, ${gsmCount} game state messages.\n\n`;

  // Annotations table — sorted by instances desc
  const annSorted = [...annotations.values()].sort((a, b) => b.instances - a.instances);
  out += "## Annotations\n\n";
  out += "| Type | Instances | Games | Persistence | Always keys |\n";
  out += "|------|-----------|-------|-------------|-------------|\n";
  for (const p of annSorted) {
    const keys = p.alwaysKeys.length > 0 ? p.alwaysKeys.join(", ") : "—";
    out += `| ${p.type} | ${p.instances} | ${p.games} | ${p.persistence} | ${keys} |\n`;
  }

  // Prompts table — sorted by occurrences desc
  const promptSorted = [...prompts.values()].sort((a, b) => b.occurrences - a.occurrences);
  out += "\n## Prompts\n\n";
  out += "| Type | Occurrences | Games | Always fields |\n";
  out += "|------|-------------|-------|---------------|\n";
  for (const p of promptSorted) {
    const fields = p.alwaysFields.length > 0 ? p.alwaysFields.join(", ") : "—";
    out += `| ${p.type} | ${p.occurrences} | ${p.games} | ${fields} |\n`;
  }

  // Actions table — sorted by occurrences desc
  const actionSorted = [...actions.values()].sort((a, b) => b.occurrences - a.occurrences);
  out += "\n## Actions\n\n";
  out += "| Type | Occurrences | Games | Always fields |\n";
  out += "|------|-------------|-------|---------------|\n";
  for (const p of actionSorted) {
    const fields = p.alwaysFields.length > 0 ? p.alwaysFields.join(", ") : "—";
    out += `| ${p.type} | ${p.occurrences} | ${p.games} | ${fields} |\n`;
  }

  return out;
}

export function writeIndex(
  annotations: Map<string, AnnotationProfile>,
  prompts: Map<string, PromptProfile>,
  actions: Map<string, ActionProfile>,
  gameCount: number,
  gsmCount: number,
  outDir: string,
): void {
  ensureDir(outDir);
  const filePath = join(outDir, "README.md");
  writeFileSync(filePath, renderIndexReadme(annotations, prompts, actions, gameCount, gsmCount));
}
