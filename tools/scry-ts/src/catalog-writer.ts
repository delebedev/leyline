// --- File I/O ---

function ensureDir(dir: string): void {
  mkdirSync(dir, { recursive: true });
}

function writeIfChanged(filePath: string, content: string): boolean {
  if (existsSync(filePath)) {
    const existing = readFileSync(filePath, "utf-8");
    if (existing === content) return false;
  }
  writeFileSync(filePath, content);
  return true;
}

function readExisting(filePath: string): string | undefined {
  if (!existsSync(filePath)) return undefined;
  return readFileSync(filePath, "utf-8");
}

export function writeAnnotations(
  profiles: Map<string, AnnotationProfile>,
  outDir: string,
): WriteResult {
  const dir = join(outDir, "annotations");
  ensureDir(dir);
  const written: string[] = [];
  const unchanged: string[] = [];

  for (const [type, profile] of profiles) {
    const filePath = join(dir, `${type}.yaml`);
    const existing = readExisting(filePath);
    const yaml = renderAnnotationYaml(profile, existing);
    if (writeIfChanged(filePath, yaml)) {
      written.push(filePath);
    } else {
      unchanged.push(filePath);
    }
  }

  return { written, unchanged };
}

